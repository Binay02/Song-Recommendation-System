def recommend_song(mood_or_genre):
    key = mood_or_genre.lower()
    if key in SONG_DATABASE:
        return random.choice(SONG_DATABASE[key])
    else:
        return ("Sorry, I couldn't find a song for that mood/genre. "
                "Try 'happy', 'sad', 'relax', 'rock', or 'pop'.")

def chatbot():
    print("Hello! I'm your Song Bot üéµ")
    print("Tell me your mood or favorite genre (e.g., happy, sad, relax, rock, pop), and I'll recommend a song!")
    while True:
        user_input = input("You: ")
        if user_input.lower() in ['exit', 'quit', 'bye']:
            print("Bot: Goodbye! Enjoy your music!")
            break
        song = recommend_song(user_input)
        print(f"Bot: {song}")

